                db.SaveChanges();
                return RedirectToAction("TagList");
            }

            return View(tag);
        }
        public ActionResult DeleteTag(int id)
        {
            Tag user = db.Tag.Find(id);
            db.Tag.Remove(user);
            db.SaveChanges();
            return RedirectToAction("TagList");
        }
    }
}
